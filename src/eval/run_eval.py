"""
src/eval/run_eval.py

Full evaluation pipeline:
  1. Load CORD v2 test split
  2. Run inference on every sample
  3. Compute CER, WER, field F1, field accuracy, latency percentiles
  4. Log metrics to MLflow
  5. Write markdown + PNG chart report to reports/

Usage:
    python src/eval/run_eval.py --split test --checkpoint checkpoints/lora/best
"""
import argparse
import csv
import json
import os
import pathlib
import sys
import time

import matplotlib.pyplot as plt
import mlflow
import numpy as np
from loguru import logger
from PIL import Image

sys.path.insert(0, str(pathlib.Path(__file__).parent.parent.parent))
from src.data.dataset import extract_target_fields
from src.eval.metrics import SCALAR_FIELDS, FinancialOCREvaluator, scalar_field_correctness
from src.model.qwen_model import QwenOCRModel

REPORTS_DIR = pathlib.Path("reports")
REPORTS_DIR.mkdir(exist_ok=True)


# ── Report generation ─────────────────────────────────────────────────────────

def _make_latency_chart(latencies: list[float], out_path: pathlib.Path) -> None:
    fig, ax = plt.subplots(figsize=(8, 4))
    ax.hist(latencies, bins=20, color="#7F77DD", edgecolor="white", linewidth=0.5)
    ax.axvline(np.percentile(latencies, 50), color="#1D9E75", linestyle="--", label="p50")
    ax.axvline(np.percentile(latencies, 95), color="#D85A30", linestyle="--", label="p95")
    ax.axvline(np.percentile(latencies, 99), color="#A32D2D", linestyle="--", label="p99")
    ax.set_xlabel("Inference latency (ms)")
    ax.set_ylabel("Count")
    ax.set_title("Latency distribution")
    ax.legend()
    fig.tight_layout()
    fig.savefig(out_path, dpi=120)
    plt.close(fig)
    logger.info(f"Latency chart saved to {out_path}")


def _write_markdown_report(metrics: dict, split: str, checkpoint: str) -> pathlib.Path:
    md = f"""# Financial OCR — Evaluation Report

**Split:** `{split}`  
**Checkpoint:** `{checkpoint}`  
**Samples:** {metrics["num_samples"]}

---

## Accuracy Metrics

| Metric | Value |
|--------|-------|
| Character Error Rate (CER) | {metrics["cer"]:.4f} |
| Word Error Rate (WER) | {metrics["wer"]:.4f} |
| Field-level Accuracy | {metrics["field_accuracy"]:.4f} |
| Field F1 Score | {metrics["field_f1"]:.4f} |
| Field Precision | {metrics["field_precision"]:.4f} |
| Field Recall | {metrics["field_recall"]:.4f} |

## Per-field Accuracy

| Field | Accuracy |
|-------|----------|
"""
    for field, acc in metrics.get("per_field_accuracy", {}).items():
        md += f"| `{field}` | {acc:.4f} |\n"

    md += f"""
## Latency Benchmarks

| Percentile | Latency (ms) |
|-----------|-------------|
| p50 | {metrics["latency_p50_ms"]:.1f} |
| p95 | {metrics["latency_p95_ms"]:.1f} |
| p99 | {metrics["latency_p99_ms"]:.1f} |

![Latency distribution](latency_histogram.png)

---
*Generated by `src/eval/run_eval.py`*
"""
    out_path = REPORTS_DIR / f"eval_{split}.md"
    out_path.write_text(md)
    logger.info(f"Markdown report saved to {out_path}")
    return out_path


def _write_prediction_artifacts(rows: list[dict], split: str) -> tuple[pathlib.Path, pathlib.Path]:
    jsonl_path = REPORTS_DIR / f"predictions_{split}.jsonl"
    csv_path = REPORTS_DIR / "error_analysis.csv"

    with jsonl_path.open("w") as f:
        for row in rows:
            f.write(json.dumps(row, ensure_ascii=False) + "\n")

    fieldnames = [
        "image_path",
        "store_name_gt",
        "store_name_pred",
        "total_price_gt",
        "total_price_pred",
        "tax_price_gt",
        "tax_price_pred",
        "store_name_correct",
        "total_price_correct",
        "tax_price_correct",
        "latency_ms",
    ]
    with csv_path.open("w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        for row in rows:
            gt = row["ground_truth"]
            pred = row["parsed_prediction"]
            correctness = row["per_field_correctness"]
            writer.writerow(
                {
                    "image_path": row["image_path"],
                    "store_name_gt": gt.get("store_name"),
                    "store_name_pred": pred.get("store_name"),
                    "total_price_gt": gt.get("total_price"),
                    "total_price_pred": pred.get("total_price"),
                    "tax_price_gt": gt.get("tax_price"),
                    "tax_price_pred": pred.get("tax_price"),
                    "store_name_correct": correctness.get("store_name"),
                    "total_price_correct": correctness.get("total_price"),
                    "tax_price_correct": correctness.get("tax_price"),
                    "latency_ms": row["latency_ms"],
                }
            )

    logger.info(f"Raw predictions saved to {jsonl_path}")
    logger.info(f"Error analysis CSV saved to {csv_path}")
    return jsonl_path, csv_path


# ── Main ──────────────────────────────────────────────────────────────────────

def main(split: str, checkpoint: str | None, processed_dir: str) -> None:
    meta_path = pathlib.Path(processed_dir) / split / "metadata.json"
    if not meta_path.exists():
        logger.error(f"No metadata at {meta_path}. Run preprocess_data.py first.")
        sys.exit(1)

    with open(meta_path) as f:
        records = json.load(f)

    # Only use non-augmented samples for eval (clean originals)
    records = [r for r in records if not r.get("augmented", False)]
    logger.info(f"Evaluating {len(records)} samples from '{split}' split")

    # ── MLflow ────────────────────────────────────────────────────────────
    mlflow.set_tracking_uri(os.getenv("MLFLOW_TRACKING_URI", "mlruns"))
    mlflow.set_experiment("financial-ocr-eval")

    with mlflow.start_run(run_name=f"eval_{split}"):
        mlflow.log_param("split", split)
        mlflow.log_param("checkpoint", checkpoint or "base")
        mlflow.log_param("num_samples", len(records))

        model = QwenOCRModel(checkpoint=checkpoint)
        evaluator = FinancialOCREvaluator()
        prediction_rows = []

        for i, rec in enumerate(records):
            image = Image.open(rec["image_path"]).convert("RGB")
            gt = rec["target"]

            t0 = time.perf_counter()
            prediction = model.predict(image)
            latency_ms = (time.perf_counter() - t0) * 1000

            evaluator.add(prediction, gt, latency_ms=latency_ms)
            correctness = scalar_field_correctness(prediction, gt)
            prediction_rows.append(
                {
                    "sample_id": rec.get("id"),
                    "image_path": rec["image_path"],
                    "ground_truth": gt,
                    "prediction": prediction.get("raw_output", prediction),
                    "parsed_prediction": prediction,
                    "latency_ms": round(latency_ms, 3),
                    "per_field_correctness": {
                        field: correctness.get(field, False) for field in SCALAR_FIELDS
                    },
                }
            )

            if (i + 1) % 20 == 0:
                logger.info(f"  {i+1}/{len(records)} evaluated")

        model.close()

        # ── Compute & log ─────────────────────────────────────────────────
        eval_result = evaluator.compute()
        metrics = eval_result.to_dict()

        mlflow.log_metrics({k: v for k, v in metrics.items() if isinstance(v, (int, float))})

        # ── Charts ────────────────────────────────────────────────────────
        chart_path = REPORTS_DIR / "latency_histogram.png"
        _make_latency_chart(eval_result.latencies_ms, chart_path)
        mlflow.log_artifact(str(chart_path))

        # ── Prediction artifacts ──────────────────────────────────────────
        predictions_path, error_analysis_path = _write_prediction_artifacts(
            prediction_rows, split
        )
        mlflow.log_artifact(str(predictions_path))
        mlflow.log_artifact(str(error_analysis_path))

        # ── Markdown report ───────────────────────────────────────────────
        report_path = _write_markdown_report(
            metrics, split=split, checkpoint=checkpoint or "base"
        )
        mlflow.log_artifact(str(report_path))

        # ── Print summary ─────────────────────────────────────────────────
        print("\n── Evaluation Summary ───────────────────────────────────────────────")
        for k, v in metrics.items():
            if not isinstance(v, dict):
                print(f"  {k:<28} {v}")
        print("─────────────────────────────────────────────────────────────────────\n")


if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument("--split", default="test", choices=["train", "validation", "test"])
    parser.add_argument("--checkpoint", default=None, help="Path to LoRA checkpoint")
    parser.add_argument("--processed-dir", default="data/processed")
    args = parser.parse_args()
    main(args.split, args.checkpoint, args.processed_dir)
